# –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è (–º–æ–∂–Ω–æ –ø–µ—Ä–µ–æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å —á–µ—Ä–µ–∑ .env.deploy)
SERVER_IP="${DEPLOY_SERVER_IP:-194.32.142.152}"
SERVER_USER="${DEPLOY_SERVER_USER:-root}"
REMOTE_DIR="${DEPLOY_REMOTE_DIR:-/var/www/altec}"
BUILD_DIR="${DEPLOY_BUILD_DIR:-./out}"

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m' # No Color

echo -e "${GREEN}üöÄ –ù–∞—á–∏–Ω–∞–µ–º –¥–µ–ø–ª–æ–π –ª–µ–Ω–¥–∏–Ω–≥–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä ${SERVER_IP}${NC}"

# –®–∞–≥ 1: –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –±–∏–ª–¥–∞
echo -e "${YELLOW}üì¶ –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –±–∏–ª–¥–∞...${NC}"
if [ ! -d "$BUILD_DIR" ]; then
    echo -e "${YELLOW}–ë–∏–ª–¥ –Ω–µ –Ω–∞–π–¥–µ–Ω. –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—ã–π –±–∏–ª–¥...${NC}"
    npm run build
    if [ $? -ne 0 ]; then
        echo -e "${RED}‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–±–æ—Ä–∫–µ –ø—Ä–æ–µ–∫—Ç–∞${NC}"
        exit 1
    fi
else
    echo -e "${GREEN}‚úÖ –ë–∏–ª–¥ –Ω–∞–π–¥–µ–Ω${NC}"
    read -p "–ü–µ—Ä–µ—Å–æ–±—Ä–∞—Ç—å –ø—Ä–æ–µ–∫—Ç? (y/n): " rebuild
    if [ "$rebuild" = "y" ] || [ "$rebuild" = "Y" ]; then
        npm run build
        if [ $? -ne 0 ]; then
            echo -e "${RED}‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–±–æ—Ä–∫–µ –ø—Ä–æ–µ–∫—Ç–∞${NC}"
            exit 1
        fi
    fi
fi

# –®–∞–≥ 2: –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ
echo -e "${YELLOW}üìÅ –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ...${NC}"
